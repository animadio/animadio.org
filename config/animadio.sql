DROP DATABASE IF EXISTS `animadio`;
CREATE DATABASE `animadio` CHARACTER SET utf8;

USE `animadio`;

CREATE TABLE `User`
(
    `id`    TINYINT       UNSIGNED  PRIMARY KEY AUTO_INCREMENT,
    `name`  VARCHAR(50)   NOT NULL  UNIQUE,
    `image` VARCHAR(50)   UNIQUE,
    `email` VARCHAR(100)  NOT NULL  UNIQUE,
    `pass`  VARCHAR(100)  NOT NULL
)
    ENGINE=INNODB DEFAULT CHARSET=utf8;

CREATE TABLE `Keyframes`
(
    `id`        TINYINT     UNSIGNED    PRIMARY KEY AUTO_INCREMENT,
    `name`      VARCHAR(10) NOT NULL    UNIQUE,
    `effect`    VARCHAR(30) NOT NULL    UNIQUE
)
    ENGINE=INNODB DEFAULT CHARSET=UTF8;

CREATE TABLE `GridProperty`
(
    `id`        TINYINT     UNSIGNED    PRIMARY KEY AUTO_INCREMENT,
    `property`  VARCHAR(20) NOT NULL    UNIQUE
)
    ENGINE=INNODB DEFAULT CHARSET=UTF8;

CREATE TABLE `GridValue`
(
    `id`    TINYINT     UNSIGNED    PRIMARY KEY AUTO_INCREMENT,
    `valor` VARCHAR(30) NOT NULL    UNIQUE
)
    ENGINE=INNODB DEFAULT CHARSET=UTF8;

CREATE TABLE `Grid`
(
    `id`        TINYINT         UNSIGNED    PRIMARY KEY     AUTO_INCREMENT,
    `class`     VARCHAR(20)     NOT NULL    UNIQUE,
    `valor`     VARCHAR(30)     NOT NULL,
    `property`  TINYINT         UNSIGNED    NOT NULL,
    `source`    TINYINT         UNSIGNED    NOT NULL,
    `media`     TINYINT         UNSIGNED    NOT NULL,
    `concat`    TINYINT(1)      NOT NULL,
    CONSTRAINT  `grid_property` FOREIGN KEY (`property`)    REFERENCES  `GridProperty`(`id`)
)
    ENGINE=INNODB DEFAULT CHARSET=UTF8;

CREATE TABLE `Elements`
(
    `id`                TINYINT     UNSIGNED    PRIMARY KEY AUTO_INCREMENT,
    `class`             VARCHAR(20) NOT NULL    UNIQUE,
    `primary_value`     VARCHAR(30) NOT NULL    UNIQUE,
    `secondary_value`   VARCHAR(30) NOT NULL,
    `third_value`       VARCHAR(30) NOT NULL,
    `source`            TINYINT     NOT NULL
)
    ENGINE=INNODB DEFAULT CHARSET=UTF8;

CREATE TABLE `StatesProperty`
(
    `id`        TINYINT     UNSIGNED    PRIMARY KEY AUTO_INCREMENT,
    `property`  VARCHAR(30) NOT NULL    UNIQUE
)
    ENGINE=INNODB DEFAULT CHARSET=UTF8;

CREATE TABLE `States`
(
    `id`        SMALLINT            UNSIGNED    PRIMARY KEY     AUTO_INCREMENT,
    `class`     VARCHAR(20)         NOT NULL    UNIQUE,
    `valor`     VARCHAR(40)         NOT NULL,
    `property`  TINYINT             UNSIGNED    NOT NULL,
    `source`    TINYINT             UNSIGNED    NOT NULL,
    `media`     TINYINT             UNSIGNED    NOT NULL,
    `concat`    TINYINT(1)          NOT NULL,
    CONSTRAINT  `states_property`   FOREIGN KEY (`property`)    REFERENCES  `StatesProperty`(`id`)
)
    ENGINE=INNODB DEFAULT CHARSET=UTF8;

CREATE TABLE `HelpersProperty`
(
    `id`        TINYINT     UNSIGNED    PRIMARY KEY AUTO_INCREMENT,
    `property`  VARCHAR(30) NOT NULL    UNIQUE
)
    ENGINE=INNODB DEFAULT CHARSET=UTF8;

CREATE TABLE `Helpers`
(
    `id`        TINYINT             UNSIGNED    PRIMARY KEY     AUTO_INCREMENT,
    `class`     VARCHAR(20)         NOT NULL    UNIQUE,
    `valor`     VARCHAR(30)         NOT NULL,
    `property`  TINYINT             UNSIGNED    NOT NULL,
    `source`    TINYINT             UNSIGNED    NOT NULL,
    `media`     TINYINT             UNSIGNED    NOT NULL,
    `concat`    TINYINT(1)          NOT NULL,
    CONSTRAINT  `helpers_property`  FOREIGN KEY (`property`)    REFERENCES  `HelpersProperty`(`id`)
)
    ENGINE=INNODB DEFAULT CHARSET=UTF8;

